def create_auth_session(
    user_id: str,
    session_token_hash: str,
    expires_at: str,
) -> None:
    now = _local_now_display()

    with get_connection() as connection:
        _execute(
            connection,
            """
            INSERT INTO auth_sessions (
                session_token_hash, user_id, created_at, expires_at, last_seen_at
            )
            VALUES (%s, %s, %s, %s, %s)
            ON CONFLICT (session_token_hash)
            DO UPDATE SET
                user_id = EXCLUDED.user_id,
                expires_at = EXCLUDED.expires_at,
                last_seen_at = EXCLUDED.last_seen_at
            """,
            (session_token_hash, user_id, now, expires_at, now),
        )
        connection.commit()


def get_active_auth_session(session_token_hash: str) -> dict[str, Any] | None:
    with get_connection() as connection:
        row = _execute(
            connection,
            """
            SELECT
                p.id,
                p.user_id,
                p.first_name,
                p.last_name,
                p.email,
                p.created_at,
                s.expires_at AS session_expires_at
            FROM auth_sessions s
            JOIN profiles p ON p.user_id = s.user_id
            WHERE s.session_token_hash = %s
            """,
            (session_token_hash,),
        ).fetchone()

    record = _row_to_dict(row)
    if record is None:
        return None

    expires_at = _parse_display_timestamp(record["session_expires_at"])
    if expires_at is None or expires_at <= datetime.now(LOCAL_TIMEZONE):
        delete_auth_session(session_token_hash)
        return None

    touch_auth_session(session_token_hash)
    return record


def touch_auth_session(session_token_hash: str) -> None:
    with get_connection() as connection:
        _execute(
            connection,
            "UPDATE auth_sessions SET last_seen_at = %s WHERE session_token_hash = %s",
            (_local_now_display(), session_token_hash),
        )
        connection.commit()


