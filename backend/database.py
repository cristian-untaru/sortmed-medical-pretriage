LOCAL_TIMEZONE = ZoneInfo("Europe/Bucharest")
DISPLAY_TIMESTAMP_FORMAT = "%d %B %Y, %H:%M"
POSTGRES_INIT_LOCK_ID = 73026101

POSTGRES_SCHEMA_SQL = """
CREATE TABLE IF NOT EXISTS profiles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT UNIQUE NOT NULL,
    first_name TEXT NOT NULL,
    last_name TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    password_hash TEXT NOT NULL,
    created_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS analysis_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id TEXT NOT NULL,
    timestamp TEXT NOT NULL,
    input_text TEXT NOT NULL,
    active_model_key TEXT,
    active_model_id TEXT,
    predicted_label TEXT,
    display_label TEXT,
    confidence DOUBLE PRECISION,
    confidence_level TEXT,
    confidence_message TEXT,
    confidence_margin DOUBLE PRECISION,
    score_self_monitor DOUBLE PRECISION,
    score_consult_gp DOUBLE PRECISION,
    score_urgent DOUBLE PRECISION,
    medquad_context_json TEXT
);

CREATE TABLE IF NOT EXISTS password_reset_codes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT NOT NULL,
    code_hash TEXT NOT NULL,
    expires_at TEXT NOT NULL,
    used_at TEXT,
    attempts INTEGER NOT NULL DEFAULT 0,
    created_at TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS auth_sessions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_token_hash TEXT UNIQUE NOT NULL,
    user_id TEXT NOT NULL,
    created_at TEXT NOT NULL,
    expires_at TEXT NOT NULL,
    last_seen_at TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_analysis_history_user_id
ON analysis_history(user_id);

CREATE INDEX IF NOT EXISTS idx_analysis_history_user_id_id
ON analysis_history(user_id, id DESC);

CREATE INDEX IF NOT EXISTS idx_password_reset_codes_email
ON password_reset_codes(email);

CREATE INDEX IF NOT EXISTS idx_auth_sessions_token_hash
ON auth_sessions(session_token_hash);

CREATE INDEX IF NOT EXISTS idx_auth_sessions_user_id
ON auth_sessions(user_id);

ALTER TABLE profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE analysis_history ENABLE ROW LEVEL SECURITY;
ALTER TABLE password_reset_codes ENABLE ROW LEVEL SECURITY;
ALTER TABLE auth_sessions ENABLE ROW LEVEL SECURITY;
"""


def _local_now_display() -> str:
    return datetime.now(LOCAL_TIMEZONE).strftime(DISPLAY_TIMESTAMP_FORMAT)


def _format_timestamp_for_display(value: str) -> str:
    if value is None:
        return value
    try:
        parsed = datetime.fromisoformat(value)
    except (ValueError, TypeError):
        return value

    return parsed.astimezone(LOCAL_TIMEZONE).strftime(DISPLAY_TIMESTAMP_FORMAT)


def _parse_display_timestamp(value: str) -> datetime | None:
    try:
        return datetime.strptime(value, DISPLAY_TIMESTAMP_FORMAT).replace(
            tzinfo=LOCAL_TIMEZONE
        )
    except (TypeError, ValueError):
        return None


def _get_database_url() -> str:
    env_url = os.getenv("SORTMED_DATABASE_URL")
    if env_url:
        return env_url

    try:
        database_config = st.secrets.get("database", {})
    except Exception:
        database_config = {}

    secrets_url = database_config.get("url")
    if secrets_url:
        return str(secrets_url)

    raise RuntimeError(
        "Supabase PostgreSQL is not configured. Add [database].url in Streamlit secrets."
    )


def get_database_backend() -> str:
    return "postgres"


def _execute(connection: Any, query: str, params: tuple[Any, ...] = ()):
    return connection.execute(query, params)


def _execute_script(connection: Any, script: str) -> None:
    for statement in script.split(";"):
        statement = statement.strip()
        if statement:
            connection.execute(statement)


def is_integrity_error(error: Exception) -> bool:
    return isinstance(error, psycopg.IntegrityError)


def _migrate_display_timestamps(
    connection: Any,
    table_name: str,
    column_name: str,
) -> None:
    rows = _execute(
        connection,
        f"SELECT id, {column_name} FROM {table_name}",
    ).fetchall()

    for row in rows:
        if row[column_name] is None:
            continue
        formatted_timestamp = _format_timestamp_for_display(row[column_name])
        if formatted_timestamp == row[column_name]:
            continue

        _execute(
            connection,
            f"UPDATE {table_name} SET {column_name} = %s WHERE id = %s",
            (formatted_timestamp, row["id"]),
        )


def _row_to_dict(row: Any | None) -> dict[str, Any] | None:
    if row is None:
        return None
    return dict(row)


def _to_float(value: Any) -> float | None:
    if value is None:
        return None
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def get_connection() -> Any:
    return psycopg.connect(_get_database_url(), row_factory=dict_row)


def init_db() -> None:
    with get_connection() as connection:
        _execute(connection, "SELECT pg_advisory_xact_lock(%s)", (POSTGRES_INIT_LOCK_ID,))
        _execute_script(connection, POSTGRES_SCHEMA_SQL)
        _migrate_display_timestamps(connection, "profiles", "created_at")
        _migrate_display_timestamps(connection, "analysis_history", "timestamp")
        _migrate_display_timestamps(connection, "password_reset_codes", "created_at")
        _migrate_display_timestamps(connection, "password_reset_codes", "used_at")
        _migrate_display_timestamps(connection, "password_reset_codes", "expires_at")
        _migrate_display_timestamps(connection, "auth_sessions", "created_at")
        _migrate_display_timestamps(connection, "auth_sessions", "expires_at")
        _migrate_display_timestamps(connection, "auth_sessions", "last_seen_at")
        connection.commit()


_db_initialized = False
_db_init_lock = Lock()


def ensure_db_initialized() -> None:
    """Call init_db() at most once per Python process."""
    global _db_initialized
    with _db_init_lock:
        if not _db_initialized:
            init_db()
            _db_initialized = True


def create_user(
    first_name: str,
    last_name: str,
    email: str,
    password_hash: str,
) -> dict[str, Any]:
    user_id = str(uuid.uuid4())
    created_at = _local_now_display()

    with get_connection() as connection:
        _execute(
            connection,
            """
            INSERT INTO profiles (
                user_id, first_name, last_name, email, password_hash, created_at
            )
            VALUES (%s, %s, %s, %s, %s, %s)
            """,
            (user_id, first_name, last_name, email, password_hash, created_at),
        )
        connection.commit()

    user = get_user_by_id(user_id)
    if user is None:
        raise RuntimeError("User was created but could not be loaded.")
    return user


