def save_analysis(user_id: str, input_text: str, analysis: dict[str, Any]) -> int:
    scores = analysis.get("scores") or {}
    medquad_context_json = json.dumps(
        analysis.get("context", []),
        ensure_ascii=False,
    )

    with get_connection() as connection:
        cursor = _execute(
            connection,
            """
            INSERT INTO analysis_history (
                user_id,
                timestamp,
                input_text,
                active_model_key,
                active_model_id,
                predicted_label,
                display_label,
                confidence,
                confidence_level,
                confidence_message,
                confidence_margin,
                score_self_monitor,
                score_consult_gp,
                score_urgent,
                medquad_context_json
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
            RETURNING id
            """,
            (
                user_id,
                _local_now_display(),
                input_text,
                analysis.get("active_model_key"),
                analysis.get("active_model_id"),
                analysis.get("label"),
                analysis.get("result"),
                _to_float(analysis.get("confidence")),
                analysis.get("confidence_level"),
                analysis.get("confidence_message"),
                _to_float(analysis.get("confidence_margin")),
                _to_float(scores.get("self_monitor")),
                _to_float(scores.get("consult_gp")),
                _to_float(scores.get("urgent")),
                medquad_context_json,
            ),
        )
        new_id = cursor.fetchone()["id"]
        connection.commit()
        return int(new_id)


def get_user_account_statistics(user_id: str) -> dict[str, Any]:
    with get_connection() as connection:
        agg = _execute(
            connection,
            """
            SELECT
                COUNT(*) AS total_analyses,
                COUNT(DISTINCT active_model_key) AS models_used_count,
                AVG(confidence) AS avg_confidence,
                MAX(confidence) AS max_confidence,
                SUM(CASE WHEN predicted_label = 'urgent' THEN 1 ELSE 0 END) AS urgent_count,
                SUM(CASE WHEN predicted_label = 'consult_gp' THEN 1 ELSE 0 END) AS consult_gp_count,
                SUM(CASE WHEN predicted_label = 'self_monitor' THEN 1 ELSE 0 END) AS self_monitor_count,
                ROUND(AVG(
                    LENGTH(TRIM(input_text))
                    - LENGTH(REPLACE(TRIM(input_text), ' ', ''))
                    + 1
                )) AS avg_word_count,
                SUM(CASE WHEN confidence_level = 'high' THEN 1 ELSE 0 END) AS conf_high,
                SUM(CASE WHEN confidence_level = 'medium' THEN 1 ELSE 0 END) AS conf_medium,
                SUM(CASE WHEN confidence_level = 'low' THEN 1 ELSE 0 END) AS conf_low
            FROM analysis_history
            WHERE user_id = %s
            """,
            (user_id,),
        ).fetchone()

        first_row = _execute(
            connection,
            "SELECT timestamp FROM analysis_history WHERE user_id = %s ORDER BY id ASC LIMIT 1",
            (user_id,),
        ).fetchone()

        latest_row = _execute(
            connection,
            "SELECT timestamp FROM analysis_history WHERE user_id = %s ORDER BY id DESC LIMIT 1",
            (user_id,),
        ).fetchone()

        rec_row = _execute(
            connection,
            """
            SELECT display_label, COUNT(*) AS cnt
            FROM analysis_history
            WHERE user_id = %s AND display_label IS NOT NULL
            GROUP BY display_label
            ORDER BY cnt DESC
            LIMIT 1
            """,
            (user_id,),
        ).fetchone()

        model_breakdown_rows = _execute(
            connection,
            """
            SELECT active_model_key, COUNT(*) AS cnt
            FROM analysis_history
            WHERE user_id = %s AND active_model_key IS NOT NULL
            GROUP BY active_model_key
            """,
            (user_id,),
        ).fetchall()

    stats: dict[str, Any] = dict(agg) if agg else {}
    model_breakdown: dict[str, int] = {}
    for row in model_breakdown_rows:
        model_key = row["active_model_key"]
        base_key = get_model_base_key(model_key) or model_key
        model_breakdown[base_key] = model_breakdown.get(base_key, 0) + int(row["cnt"])

    stats["first_analysis_date"] = first_row["timestamp"] if first_row else None
    stats["latest_analysis_date"] = latest_row["timestamp"] if latest_row else None
    stats["models_used_count"] = len(model_breakdown)
    stats["most_used_model"] = (
        max(model_breakdown.items(), key=lambda item: item[1])[0]
        if model_breakdown
        else None
    )
    stats["most_common_recommendation"] = rec_row["display_label"] if rec_row else None
    stats["model_breakdown"] = model_breakdown
    return stats


def delete_user_account(user_id: str, email: str) -> bool:
    with get_connection() as connection:
        _execute(
            connection,
            "DELETE FROM auth_sessions WHERE user_id = %s",
            (user_id,),
        )
        _execute(
            connection,
            "DELETE FROM analysis_history WHERE user_id = %s",
            (user_id,),
        )
        _execute(
            connection,
            "DELETE FROM password_reset_codes WHERE email = %s",
            (email,),
        )
        cursor = _execute(
            connection,
            "DELETE FROM profiles WHERE user_id = %s",
            (user_id,),
        )
        connection.commit()
        return cursor.rowcount > 0


