def create_reset_code(email: str, code_hash: str, expires_at: str) -> None:
    with get_connection() as connection:
        _execute(
            connection,
            "UPDATE password_reset_codes SET used_at = %s WHERE email = %s AND used_at IS NULL",
            (_local_now_display(), email),
        )
        _execute(
            connection,
            """
            INSERT INTO password_reset_codes (email, code_hash, expires_at, created_at)
            VALUES (%s, %s, %s, %s)
            """,
            (email, code_hash, expires_at, _local_now_display()),
        )
        connection.commit()


def get_active_reset_code(email: str) -> dict[str, Any] | None:
    with get_connection() as connection:
        rows = _execute(
            connection,
            """
            SELECT * FROM password_reset_codes
            WHERE email = %s AND used_at IS NULL AND attempts < 5
            ORDER BY id DESC
            """,
            (email,),
        ).fetchall()

    now = datetime.now(LOCAL_TIMEZONE)
    for row in rows:
        record = _row_to_dict(row)
        try:
            expires_at = datetime.strptime(
                record["expires_at"], DISPLAY_TIMESTAMP_FORMAT
            ).replace(tzinfo=LOCAL_TIMEZONE)
            if expires_at > now:
                return record
        except (ValueError, KeyError):
            pass
    return None


